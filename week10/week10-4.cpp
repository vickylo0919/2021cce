#include <stdio.h>
int a[100]={0,0,0,0}; ///沒有寫的，都會補0
///外面宣告的變數,可以比較多memory空間
///0還沒被殺死, 1:被殺死!!
int main()
{   ///依序去找
    for(int i=2 ; i<100 ; i++){
    ///找到沒被殺掉的質數,當國王
        ///a[i] 查查看他有沒有被殺死
        if (a[i]==0){ ///王子i, 沒死的王子i
            printf("%d ",i); ///會變國王i,
            ///(2)殺光他的兄弟
            for (int k=i+i ; k<100 ; k=k+i) a[k]=1;
        }
    }
}
